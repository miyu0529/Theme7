package Theme7.demo;

public class Name {

    private String givenName;
    private String firstName;

    public Name(String givenName, String firstName) {
        this.givenName = givenName;
        this.firstName = firstName;
    }

    public String getGivenName() {
        return givenName;
    }

    public String getFirstName() {
        return firstName;
    }
}
